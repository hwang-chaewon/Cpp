#include <iostream>

using namespace std;

int main() {

	int x = 0;
	cout << x << "의 주소는" << &x << endl;

	//이렇게 함수도 없고 뭐 없는데 생뚱맞게 {}가 들어가도 괜찮은가?
	//A. 괜찮다.
	{
		int x = 0;
		cout << x << "의 주소는" << &x << endl;
	}
	{
		x = 5;
		cout << x << "의 주소는" << &x << endl;
	}
	cout << x << "의 주소는" << &x << endl;

	return 0;
}
